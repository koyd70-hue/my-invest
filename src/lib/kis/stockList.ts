export function searchStockList(query: string): StockEntry[] {
  const q = query.trim();
  if (!q) return [];
  const isCode = /^\d+$/.test(q);
  if (isCode) {
    const padded = q.padStart(6, '0');
    return STOCK_LIST.filter((s) => s.code.startsWith(padded) || s.code === padded);
  }
  return STOCK_LIST.filter(
    (s) => s.name.includes(q) || s.code.includes(q)
  ).slice(0, 20);
}
